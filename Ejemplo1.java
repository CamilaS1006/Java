public class Ejemplo1 {

    public static void main(String[] args) {
        //byte= 8 bits -128 a +127
        byte x=8;
        System.out.println(x);

        //short 16 bits
        Short b=-568;
        System.out.println(b);
        //ejemplo con concaenacion
        //system.out.println("la variable short es: " +b);
        //int 32 bits
        int a=23;
        int B=56;
        int suma1;
        System.out.println(a+B);

        suma1=a+B;
        System.out.println("la suma es:" +suma1);

        //long 64 bits
        long D=8797L;
        System.out.println("el dato primitivo es:" +D);

        //float 32 bits
        float F=3.14F;
        System.out.println("el flotante es:" +F);

        //Double 64 bits
        double d=3.23D;
        System.out.println("el double es:" +d);

        //char
        char g='1';
        System.out.println(g);

        //boolean
        boolean h=true;
        System.out.println(h);

        




    }
}
